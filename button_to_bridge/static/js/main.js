function t(key) {
    const lang = document.getElementById('langSwitcher').value;
    return (translations[lang] && translations[lang][key]) || translations['en'][key] || key;
}

function changeLanguage() {
    const lang = document.getElementById('langSwitcher').value;
    localStorage.setItem('smart_crm_lang', lang);
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.getAttribute('data-i18n'));
    });
    const selectOpt = document.querySelector('[data-i18n-opt="selectAccount"]');
    if (selectOpt) selectOpt.textContent = t('selectAccount');
    fetchCustomers();
}

function showAlert(message, type) {
    const container = document.getElementById('alert-container');
    container.innerHTML = `
        <div class="alert alert-${type}">
            <i class="fas ${type === 'success' ? 'fa-check-circle' : 'fa-exclamation-circle'}"></i>
            ${message}
        </div>
    `;
    setTimeout(() => { container.innerHTML = ''; }, 5000);
}

document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('smart_crm_lang') || 'en';
    document.getElementById('langSwitcher').value = saved;
    changeLanguage();
});

async function fetchCustomers() {
    try {
        const response = await fetch('api/customers');
        const data = await response.json();

        const tbody = document.getElementById('customers-body');
        tbody.innerHTML = '';

        if (data.length === 0) {
            tbody.innerHTML = `<tr><td colspan="4" style="text-align:center;padding:40px;color:var(--text-muted);">—</td></tr>`;
            return;
        }

        data.forEach(customer => {
            const tr = document.createElement('tr');
            let badgeClass = customer.department === 'Support' ? 'active' : 'new';
            tr.innerHTML = `
                <td>${escapeHTML(customer.name)}</td>
                <td><span class="badge ${badgeClass}">${escapeHTML(customer.department)}</span></td>
                <td>${escapeHTML(customer.phone)}</td>
                <td>
                    <div class="actions-flex">
                        <button class="btn btn-primary" onclick="initiateCall('${escapeHTML(customer.phone)}', this)" title="Call via Hipcall">
                            <i class="fas fa-phone"></i> ${t('callBtn')}
                        </button>
                        <button class="btn btn-secondary" onclick="editCustomer(${customer.id}, '${escapeHTML(customer.name)}', '${escapeHTML(customer.department)}', '${escapeHTML(customer.phone)}')">
                            <i class="fas fa-edit"></i>
                        </button>
                        <button class="btn btn-danger" onclick="deleteCustomer(${customer.id})">
                            <i class="fas fa-trash"></i>
                        </button>
                    </div>
                </td>
            `;
            tbody.appendChild(tr);
        });

    } catch(err) {
        console.error(err);
        showAlert(t('errLoadCustomers'), 'danger');
    }
}

function escapeHTML(str) {
    return str.replace(/[&<>'"]/g,
        tag => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', "'": '&#39;', '"': '&quot;' }[tag] || tag)
    );
}

async function initiateCall(phoneNumber, buttonElement) {
    const manageId = document.getElementById('account-selector').value;
    if (!manageId) {
        showAlert(t('errSelectAccount'), 'danger');
        return;
    }

    const originalText = buttonElement.innerHTML;
    buttonElement.disabled = true;
    buttonElement.innerHTML = '<i class="fas fa-spinner fa-spin"></i>';

    try {
        const response = await fetch('api/call', {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ callee_number: phoneNumber, manage_id: manageId })
        });

        const data = await response.json();

        if (response.ok) {
            showAlert(t('successCall'), 'success');
            buttonElement.innerHTML = '<i class="fas fa-check"></i>';
            setTimeout(() => {
                buttonElement.innerHTML = originalText;
                buttonElement.disabled = false;
            }, 3000);
        } else {
            let errorMsg = data.error || 'Unknown error';
            if (data.hipcall_response?.errors?.detail) {
                errorMsg += `: ${data.hipcall_response.errors.detail}`;
            } else if (data.hipcall_response?.message) {
                errorMsg += `: ${data.hipcall_response.message}`;
            }
            showAlert(`${t('errCall')}: ${errorMsg}`, 'danger');
            buttonElement.innerHTML = originalText;
            buttonElement.disabled = false;
        }

    } catch (error) {
        console.error('Call failed', error);
        showAlert(t('errNetwork'), 'danger');
        buttonElement.innerHTML = originalText;
        buttonElement.disabled = false;
    }
}

function openModal() {
    document.getElementById('customer_id').value = '';
    document.getElementById('name').value = '';
    document.getElementById('phone').value = '';
    document.getElementById('modal-title').innerHTML = `<i class="fas fa-user-plus"></i> ${t('addCustomer')}`;
    document.getElementById('customer-modal').style.display = 'flex';
}

function closeModal() {
    document.getElementById('customer-modal').style.display = 'none';
}

function editCustomer(id, name, department, phone) {
    document.getElementById('customer_id').value = id;
    document.getElementById('name').value = name;
    document.getElementById('department').value = department;
    document.getElementById('phone').value = phone;
    document.getElementById('modal-title').innerHTML = `<i class="fas fa-edit"></i> ${t('editCustomer')}`;
    document.getElementById('customer-modal').style.display = 'flex';
}

async function submitCustomer(e) {
    e.preventDefault();
    const id = document.getElementById('customer_id').value;
    const name = document.getElementById('name').value;
    const department = document.getElementById('department').value;
    const phone = document.getElementById('phone').value;

    const method = id ? 'PUT' : 'POST';
    const url = id ? `api/customers/${id}` : 'api/customers';

    try {
        const res = await fetch(url, {
            method,
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ name, department, phone })
        });

        if (res.ok) {
            closeModal();
            showAlert(t('successSave'), 'success');
            fetchCustomers();
        } else {
            const data = await res.json();
            showAlert(data.error || t('errSave'), 'danger');
        }
    } catch(err) {
        console.error(err);
        showAlert(t('errNetworkShort'), 'danger');
    }
}

async function deleteCustomer(id) {
    if (!confirm(t('confirmDelete'))) return;
    try {
        const res = await fetch(`api/customers/${id}`, { method: 'DELETE' });
        if (res.ok) {
            showAlert(t('successDelete'), 'success');
            fetchCustomers();
        }
    } catch(err) {
        console.error(err);
        showAlert(t('errNetworkShort'), 'danger');
    }
}
